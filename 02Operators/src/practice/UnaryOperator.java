package practice;

public class UnaryOperator {
    public static void main(String[] args) {

        int a = 5;
        int b = 12;

        System.out.println(a++);  //post increment  ++a pre increment
        System.out.println(a);
        System.out.println(--b); //pre decrement  b-- post decrement
        System.out.println(b);
    }
}
